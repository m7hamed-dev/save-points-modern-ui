/// Direction for bottom sheet animations
enum BottomsheetAnimationDirection {
  /// Slide from bottom (default)
  fromBottom,
  
  /// Slide from left
  fromLeft,
  
  /// Slide from right
  fromRight,
  
  /// Fade in/out
  fade,
  
  /// Scale from center
  scale,
  
  /// No animation
  none,
}

