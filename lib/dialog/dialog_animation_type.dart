/// Dialog animation types
enum DialogAnimationType {
  /// Fade in with slight upward slide
  fadeSlide,

  /// Scale from center
  scale,

  /// Slide from bottom
  slideBottom,

  /// Slide from top
  slideTop,

  /// Slide from left
  slideLeft,

  /// Slide from right
  slideRight,

  /// Bounce effect
  bounce,

  /// Rotate and scale
  rotateScale,

  /// Elastic bounce
  elastic,

  /// No animation
  none,
}
