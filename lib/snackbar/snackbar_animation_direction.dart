/// Direction for snackbar animations
enum SnackbarAnimationDirection {
  /// Slide from top
  fromTop,
  
  /// Slide from bottom
  fromBottom,
  
  /// Slide from left
  fromLeft,
  
  /// Slide from right
  fromRight,
  
  /// Fade in/out (no slide)
  fade,
  
  /// Scale from center
  scale,
  
  /// Scale and rotate
  rotateScale,
  
  /// Bounce effect
  bounce,
  
  /// Elastic effect
  elastic,
  
  /// No animation
  none,
}

